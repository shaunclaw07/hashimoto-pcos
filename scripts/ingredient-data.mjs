// ── Whitelist-check function ────────────────────────────────────────────────────
/**
 * Returns true if the normalized (lowercase, trimmed) ingredient segment
 * is a known/valid food ingredient.
 */
export function isKnownIngredient(normalized) {
  if (!normalized || normalized.length < 2) return false;
  return KNOWN_INGREDIENTS.has(normalized);
}
