// ── E-number whitelist (all EU food additive numbers) ────────────────────────
// Generates all valid E-number ranges per EU food additive regulations.
// Having extra invalid codes in the set is harmless (they won't appear in real data).
function generateENumbers() {
  const s = new Set();
  for (let n = 100; n <= 199; n++) {
    s.add('e' + n);
    s.add('e' + n + 'a');
    s.add('e' + n + 'b');
  }
  for (let n = 200; n <= 299; n++) s.add('e' + n);
  for (let n = 300; n <= 399; n++) s.add('e' + n);
  for (let n = 400; n <= 499; n++) s.add('e' + n);
  for (let n = 500; n <= 599; n++) s.add('e' + n);
  for (let n = 600; n <= 699; n++) s.add('e' + n);
  for (let n = 700; n <= 799; n++) s.add('e' + n);
  for (let n = 800; n <= 899; n++) s.add('e' + n);
  for (let n = 900; n <= 999; n++) s.add('e' + n);
  for (let n = 1100; n <= 1799; n++) s.add('e' + n);
  return s;
}
const E_NUMBERS = generateENumbers();

